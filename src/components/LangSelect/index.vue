<template>
  <el-dropdown trigger="click" class="international" @command="handleSetLanguage">
    <div>
      <svg-icon class-name="international-icon" icon-class="language" />
    </div>
    <el-dropdown-menu slot="dropdown">
      <el-dropdown-item :disabled="language==='zh-CN'" command="zh-CN">中文</el-dropdown-item>
      <el-dropdown-item :disabled="language==='en-US'" command="en-US">English</el-dropdown-item>
      <el-dropdown-item :disabled="language==='hy-AM'" command="hy-AM">Armenian</el-dropdown-item>
      <el-dropdown-item :disabled="language==='ru-RU'" command="ru-RU">Russian</el-dropdown-item>
    </el-dropdown-menu>
  </el-dropdown>
</template>

<script>
export default {
  computed: {
    language() {
      return this.$store.getters.language;
    }
  },
  methods: {
    handleSetLanguage(lang) {
      this.$i18n.locale = lang;
      this.$store.dispatch('setLanguage', lang);
      this.$message({
        message: 'Switch Language Success',
        type: 'success'
      });
      location.reload();
    }
  }
};
</script>

<style scoped>
.international-icon {
  font-size: 20px;
  cursor: pointer;
  vertical-align: -5px!important;
}
</style>

